# =========================================================
# Safe Git Commit Script (Single Dev / Local Priority)
# =========================================================
# 目的:
# - 一人開発用の安全なコミット + push
# - バックアップを作るが「暴走しない設計」
# - Next.jsなどのビルド成果物を絶対に巻き込まない
# - Gitのエラー原因（長すぎるパス・無限コピー）を防ぐ
# =========================================================

Write-Host "=== Safe Commit Tool ==="

# ---------------------------------------------------------
# ■ 重要：文字化け・環境依存対策
# ---------------------------------------------------------
[Console]::OutputEncoding = [System.Text.Encoding]::UTF8

# ---------------------------------------------------------
# ■ バックアップ作成
# ---------------------------------------------------------
# ★今回の問題の本質：
# 以前のスクリプトでは「.next や node_modules を含めて丸ごとコピー」
# していたため、バックアップの中にさらにバックアップが入り、
# 無限にフォルダが深くなる構造になっていた。
#
# その結果：
# - パスが異常に長くなる
# - Gitが "Filename too long" でエラー
# - .next（ビルド成果物）が大量コピーされる
#
# → これを防ぐため robocopy で除外指定する
# ---------------------------------------------------------

$timestamp = Get-Date -Format "yyyyMMdd_HHmmss"
$backup = ".\backup\commit_$timestamp"

New-Item -ItemType Directory -Force -Path $backup | Out-Null

# robocopyで安全コピー（除外付き）
robocopy . $backup /E `
    /XD node_modules .next backup .git `
    /NFL /NDL /NJH /NJS /NP > $null

Write-Host "バックアップ作成: $backup"

# ---------------------------------------------------------
# ■ Git状態確認
# ---------------------------------------------------------
$status = git status --porcelain

if (-not $status) {
    Write-Host "変更なし → コミット不要"
    exit 0
}

# ---------------------------------------------------------
# ■ add（全部確実に含める）
# ---------------------------------------------------------
git add -A

if ($LASTEXITCODE -ne 0) {
    Write-Host "git add 失敗"
    exit 1
}

# ---------------------------------------------------------
# ■ コミットメッセージ入力
# ---------------------------------------------------------
$msg = Read-Host "コミットメッセージ"

if ([string]::IsNullOrWhiteSpace($msg)) {
    Write-Host "中断：メッセージなし"
    exit 1
}

git commit -m "$msg"

if ($LASTEXITCODE -ne 0) {
    Write-Host "コミット失敗"
    exit 1
}

# ---------------------------------------------------------
# ■ push（同期）
# ---------------------------------------------------------
git push

if ($LASTEXITCODE -ne 0) {
    Write-Host "push失敗"
    exit 1
}

# ---------------------------------------------------------
# ■ 完了
# ---------------------------------------------------------
Write-Host "=== 完了：安全にコミット＋同期しました ==="